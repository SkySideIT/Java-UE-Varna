    private void jTable1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyReleased
        int code = evt.getKeyCode();
        if (code == KeyEvent.VK_DELETE) {
            int selection = jTable1.getSelectedRow();
            String id = String.valueOf(t.getValueAt(selection, 0));
            JOptionPane.showMessageDialog(this, "Успешно изтрихте играта!", "Успешна операция!", 1);
            query.delete("Video_games", "VideoID", id);
            t.removeRow(selection);
        }
    }//GEN-LAST:event_jTable1KeyReleased

