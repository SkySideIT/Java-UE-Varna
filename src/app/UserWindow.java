    private void jTable1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseReleased
        if (SwingUtilities.isRightMouseButton(evt)) {
            jPopupMenu1.show(evt.getComponent(), evt.getX(), evt.getY());
            int row = jTable1.rowAtPoint(evt.getPoint());
            jTable1.setRowSelectionInterval(row, row);
        }
    }//GEN-LAST:event_jTable1MouseReleased

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        int selection = jTable1.getSelectedRow();
        String id = String.valueOf(t.getValueAt(selection, 0));
        JOptionPane.showMessageDialog(this, "Успешно изтрихте играта!", "Успешна операция!", 1);
        query.delete("Video_games", "VideoID", id);
        t.removeRow(selection);
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        t.setRowCount(0);
        ArrayList<String> data = new ArrayList<String>();
        String[] columns = {"VideoID", "Name", "Version", "Num_available", "Price"};
        String tursene = jTextField1.getText();
        data = query.selectWhere(columns, "Video_games", new int[]{1}, new String[]{tursene});
        for (int i = 0; i < data.size(); i++) {
            String[] row = data.get(i).split("---");
            t.addRow(row);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

