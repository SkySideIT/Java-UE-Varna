    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText();
        String password = jPasswordField1.getText();

        ArrayList<String> data = query.selectWherelogin(new String[]{"Username", "Password", "Status"}, "Users", new int[]{0, 1},
                new String[]{username, password});
        if (data.size() == 1) {

            String[] row = data.get(0).split("---");
            if (row[2].equals("admin")) {
                JOptionPane.showMessageDialog(this, "Здравей, шефе!", "Успешно влизане!", 1);

                new AdminWindow().show();
                this.dispose();

            } 
            else {
                if (row[2].equals("user")) {
                    JOptionPane.showMessageDialog(this, "Добре дошли!", "Успешно влизане!", 1);

                    new UserWindow().show();
                    this.dispose();
                }
            }       
        } 
        else {
            JOptionPane.showMessageDialog(this, "Невалидни данни!", "Грешка!", 0);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

